class RealisticSimStats:
    """Statistics for the realistic simulation."""
    
    def __init__(self):
        self.reset()
    
    def reset(self):
        self.total_days = 0
        self.days_with_sessions = 0
        self.days_with_backfill = 0
        self.days_skipped = 0
        self.total_sessions = 0
        self.total_turns = 0
        self.total_l1_generated = 0
        self.total_l2_generated = 0
        self.total_l3_generated = 0
        self.total_l4_generated = 0
        self.total_l5_generated = 0
        self.inter_session_l2_count = 0
        self.daily_backfill_count = 0
        self.force_backfill_count = 0
        self.daily_logs = []  # Logs for daily processing.
        self.start_time = None
        self.end_time = None
    
    def log_day(self, day: date, log_entry: Dict[str, Any]):
        """Record the daily processing log."""
        self.daily_logs.append({
            "date": day,
            **log_entry
        })
    
    def get_summary(self):
        if self.start_time and self.end_time:
            total_time = self.end_time - self.start_time
        else:
            total_time = 0
        
        return {
            "total_days": self.total_days,
            "days_with_sessions": self.days_with_sessions,
            "days_with_backfill": self.days_with_backfill,
            "days_skipped": self.days_skipped,
            "total_sessions": self.total_sessions,
            "total_turns": self.total_turns,
            "total_l1_generated": self.total_l1_generated,
            "total_l2_generated": self.total_l2_generated,
            "total_l3_generated": self.total_l3_generated,
            "total_l4_generated": self.total_l4_generated,
            "total_l5_generated": self.total_l5_generated,
            "inter_session_l2_count": self.inter_session_l2_count,
            "daily_backfill_count": self.daily_backfill_count,
            "force_backfill_count": self.force_backfill_count,
            "total_execution_time": total_time
        }


async def register_conversation_users(speakers, conv_id):
    """Register conversation users and experts."""
    try:
        from services.user_service import get_user_service
        from services.character_service import get_character_service
        import uuid
        import time
        
        user_service = get_user_service()
        character_service = get_character_service()
        
        timestamp_suffix = int(time.time() * 1000) % 100000
        unique_suffix = str(uuid.uuid4())[:8]
        
        unique_username = f"{speakers[0]}_{conv_id}_real_{timestamp_suffix}_{unique_suffix}"
        user = await user_service.create_user(
            username=unique_username,
            display_name=f"{speakers[0]} ({conv_id})",
            description=f"[REALISTIC_SIM] {conv_id} conversation participant (user): {speakers[0]}",
            metadata={
                "conv_id": conv_id,
                "original_name": speakers[0],
                "role": "user",
                "test_type": "realistic_sim"
            }
        )
        user_id = user["id"]
        
        timestamp_suffix_2 = int(time.time() * 1000) % 100000 + 1
        unique_suffix_2 = str(uuid.uuid4())[:8]
        
        unique_expert_name = f"{speakers[1]}_{conv_id}_real_{timestamp_suffix_2}_{unique_suffix_2}"
        expert_character = await character_service.create_character(
            name=unique_expert_name,
            character_type="expert",
            display_name=f"{speakers[1]} ({conv_id})",
            description=f"[REALISTIC_SIM] {conv_id} conversation participant (expert): {speakers[1]}",
            metadata={
                "conv_id": conv_id,
                "original_name": speakers[1],
                "role": "expert",
                "test_type": "realistic_sim"
            }
        )
        expert_id = expert_character["id"]
        
        print(f"[REALISTIC_SIM] Registered {conv_id}: {speakers[0]} (user) & {speakers[1]} (expert)")
        return user_id, expert_id
        
    except Exception as e:
        logger.error(f"User registration failed for {conv_id}: {e}")
        raise RuntimeError(f"User registration failed for {conv_id}: {e}")


async def process_realistic_simulation(
    conv_id: str,
    conv_data: dict,
    conversation_users: dict,
    service: MemoryGenerationService,
    stats: RealisticSimStats,
    conv_idx: int,
    total_convs: int,
    stats_helper: Optional[StatsTestHelper] = None
) -> dict:
    """
    Realistic system simulation processing
    
    Process:
    1. Analyzes session times to get the first and last dates.
    2. Organizes sessions by date.
    3. Simulates day by day:
       - On days with sessions, it processes them. If there are multiple sessions in a day, it performs an inter-session L2 backfill.
       - At midnight daily, it triggers an auto-backfill to detect memories from the previous day.
    4. On the last day + 1, it uses force mode for a forced backfill.
    """
    sessions_data = conv_data["sessions"]
    speakers = conv_data["speakers"]
    user_id = conversation_users[conv_id]["user_id"]
    expert_id = conversation_users[conv_id]["expert_id"]
    
    print(f"\n{'='*80}")
    print(f"[REALISTIC_SIM] Start realistic system simulation: {conv_id}")
    print(f"[REALISTIC_SIM] User: {speakers[0]}, Expert: {speakers[1]}")
    print(f"{'='*80}\n")
    
    # 1. Analyze and organize sessions by date (processing all sessions).
    sessions_by_date = defaultdict(list)
    session_details = {}
    
    for session_id, session_data in sessions_data.items():
        date_time_str = session_data.get("date_time", "")
        try:
            session_time = time_parser.parse(date_time_str)
            session_date = session_time.date()
            
            sessions_by_date[session_date].append({
                "session_id": session_id,
                "session_time": session_time,
                "session_data": session_data
            })
            
            session_details[session_id] = {
                "time": session_time,
                "date": session_date,
                "dialogues": session_data.get("dialogues", [])
            }
        except Exception as e:
            logger.error(f"Failed to parse session time {session_id}: {e}")
            continue
    
    if not sessions_by_date:
        print(f"[REALISTIC_SIM] No valid session data")
        return {"success": False, "error": "No valid sessions"}
    
    # 2. Get the date range.
    all_dates = sorted(sessions_by_date.keys())
    first_date = all_dates[0]
    last_date = all_dates[-1]
    simulation_end_date = last_date + timedelta(days=1)  # Simulate until the day after the last session.
    
    print(f"[REALISTIC_SIM] Time range: {first_date} to {last_date}")
    print(f"[REALISTIC_SIM] Simulation days: {(simulation_end_date - first_date).days + 1} days")
    print(f"[REALISTIC_SIM] Session days: {len(sessions_by_date)} days")
    print(f"[REALISTIC_SIM] Total sessions: {sum(len(sessions) for sessions in sessions_by_date.values())}")
    print()
    
    # 3. Simulate day by day.
    current_date = first_date
    total_turns = 0
    total_l1 = 0
    total_l2 = 0
    total_l3 = 0
    total_l4 = 0
    total_l5 = 0
    inter_session_l2 = 0
    daily_backfill = 0
    force_backfill = 0
    simulation_interrupted = False
    
    while current_date <= simulation_end_date:
        day_log = {
            "has_sessions": False,
            "sessions_count": 0,
            "turns_count": 0,
            "backfill_triggered": False,
            "force_mode": False,
            "l1_generated": 0,
            "l2_generated": 0,
            "l3_generated": 0,
            "l4_generated": 0,
            "l5_generated": 0
        }
        
        print(f"\n{'─'*80}")
        print(f"📅 [{current_date}] Start simulation")
        print(f"{'─'*80}")
        
        # ✅ Fix: Execute midnight backfill first, then process daily sessions to match the real system's order of operations.
        # Perform daily auto-backfill at midnight to detect memories from the previous day.
        if current_date > first_date:  # Start from the second day.
            print(f"\n  🌙 [02:00 AM] Trigger auto-backfill detection")
            day_log["backfill_triggered"] = True
            
            try:
                # Perform a daily routine backfill to detect missing memories from the previous day.
                print(f"      🔍 Detecting missing memories from the previous day...")
                
                yesterday = current_date - timedelta(days=1)
                detector = CatchUpDetector()
                tasks = await detector.detect_missing_in_recent_months(
                    user_id=user_id,
                    expert_id=expert_id,
                    month_count=2,
                    force=False,
                    force_timestamp=datetime.combine(current_date, datetime.min.time()) + timedelta(hours=2)
                )
                daily_backfill += 1
                
                if tasks:
                    sorter = BackfillTaskSorter()
                    sorted_tasks = sorter.sort_tasks(tasks)
                    
                    print(f"      📋 Pending backfill tasks: {len(sorted_tasks)}")
                    for task in sorted_tasks[:5]:  # Show only the first 5 tasks.
                        print(f"          - {task.layer}: {task.session_id or task.time_window}")
                    if len(sorted_tasks) > 5:
                        print(f"          ... {len(sorted_tasks) - 5} more tasks")
                    
                    result = await service.run_backfill(sorted_tasks)
                    
                    # Count the generated memories.
                    generated_memories = result.memories if hasattr(result, 'memories') else result.get("generated_memories", [])
                    for memory in generated_memories:
                        # Try multiple ways to get the memory level.
                        mem_level = None
                        if hasattr(memory, 'level'):
                            mem_level = memory.level
                        elif isinstance(memory, dict) and 'level' in memory:
                            mem_level = memory['level']
                            
                        if mem_level:
                            mem_level_str = mem_level.value if hasattr(mem_level, 'value') else str(mem_level)
                            if "L2" in mem_level_str:
                                total_l2 += 1
                                day_log["l2_generated"] += 1
                            elif "L3" in mem_level_str:
                                total_l3 += 1
                                day_log["l3_generated"] += 1
                            elif "L4" in mem_level_str:
                                total_l4 += 1
                                day_log["l4_generated"] += 1
                            elif "L5" in mem_level_str:
                                total_l5 += 1
                                day_log["l5_generated"] += 1
                    
                    # 🆕 Record statistics.
                    if stats_helper:
                        stats_helper.record_memories_from_result(
                            result,
                            trigger_type="Daily auto-backfill"
                        )
                    
                    print(f"      ✅ Auto-backfill completed: L2={day_log['l2_generated']}, L3={day_log['l3_generated']}, L4={day_log['l4_generated']}, L5={day_log['l5_generated']}")
                else:
                    print(f"      ⏭ No backfill needed")
                    
            except Exception as e:
                logger.error(f"Auto-backfill exception: {e}")
                print(f"      ✗ Auto-backfill exception: {e}")
        
        # Check if there are any sessions on this day.
        if current_date in sessions_by_date:
            day_log["has_sessions"] = True
            day_sessions = sessions_by_date[current_date]
            day_sessions.sort(key=lambda s: s["session_time"])
            
            print(f"✅ {len(day_sessions)} sessions today.")
            
            day_log["sessions_count"] = len(day_sessions)
            
            # Process all sessions for this day.
            for session_idx, session_info in enumerate(day_sessions, 1):
                session_id = session_info["session_id"]
                session_time = session_info["session_time"]
                session_data = session_info["session_data"]
                dialogues = session_data.get("dialogues", [])
                
                print(f"\n  📝 Session {session_idx}/{len(day_sessions)}: {conv_id}_{session_id} ({len(dialogues)} dialogues).")
                print(f"      Session start time: {session_time.strftime('%Y-%m-%d %H:%M:%S')}")
                
                # Process all dialogue turns
                total_possible_turns = len(dialogues) // 2
                max_turns = total_possible_turns  # Process all dialogue turns.
                session_last_turn_timestamp = session_time  # Record the timestamp of the last turn in the current session.
                is_last_session = (session_idx == len(day_sessions) and current_date == last_date)  # Check if this is the last session.
                
                print(f"      Processing all {max_turns} dialogue turns (total {total_possible_turns} turns).")
                
                for turn_idx in range(1, max_turns + 1):
                    dialogue_start_idx = (turn_idx - 1) * 2
                    dialogue_end_idx = dialogue_start_idx + 1
                    
                    if dialogue_end_idx >= len(dialogues):
                        break
                    
                    first_dialogue = dialogues[dialogue_start_idx]
                    second_dialogue = dialogues[dialogue_end_idx]
                    first_text = first_dialogue.get("text", "")
                    second_text = second_dialogue.get("text", "")
                    first_speaker = first_dialogue.get("speaker", "")
                    second_speaker = second_dialogue.get("speaker", "")
                    
                    # 🔧 Change: The dialogue interval has been changed from 2 minutes to 5 seconds.
                    turn_timestamp = session_time + timedelta(seconds=(turn_idx - 1) * 5)
                    session_last_turn_timestamp = turn_timestamp  # Update the timestamp of the last turn in the current session.
                    content = f"{first_speaker}: {first_text}\n{second_speaker}: {second_text}"
                    
                    # Call service.generate_memory() to generate L1 memories.
                    try:
                        request = MemoryGenerationRequest(
                            user_id=user_id,
                            expert_id=expert_id,
                            session_id=f"{conv_id}_{session_id}",
                            content=content,
                            timestamp=turn_timestamp,
                            metadata={
                                "conv_id": conv_id,
                                "session_id": session_id,
                                "turn_idx": turn_idx,
                                "speakers": speakers
                            }
                        )
                        
                        result = await service.generate_memory(request)
                        
                        if result.success:
                            total_turns += 1
                            day_log["turns_count"] += 1
                            
                            # Count L1 memories.
                            generated_memories = result.memories if hasattr(result, 'memories') else []
                            for mem in generated_memories:
                                # Try multiple ways to get the memory level.
                                mem_level = None
                                if hasattr(mem, 'level'):
                                    mem_level = mem.level
                                elif isinstance(mem, dict) and 'level' in mem:
                                    mem_level = mem['level']
                                    
                                if mem_level:
                                    mem_level_str = mem_level.value if hasattr(mem_level, 'value') else str(mem_level)
                                    if "L1" in mem_level_str:
                                        total_l1 += 1
                                        day_log["l1_generated"] += 1
                            
                            # 🆕 Record statistics.
                            if stats_helper:
                                stats_helper.record_memories_from_result(
                                    result,
                                    trigger_type="Real-time generation",
                                    turn_id=f"{conv_id}_{session_id}_turn_{turn_idx}"
                                )
                            
                            print(f"      ✓ Turn {turn_idx} processed successfully (time: {turn_timestamp.strftime('%Y-%m-%d %H:%M:%S')})")
                        else:
                            print(f"      ✗ Turn {turn_idx} processing failed: {result.error or 'Unknown error'}")
                            
                    except KeyboardInterrupt:
                        print(f"\n      ⚠️ User interrupted. Stopping dialogue processing...")
                        simulation_interrupted = True
                        break
                    except Exception as e:
                        logger.error(f"Turn {turn_idx} processing exception: {e}")
                        print(f"      ✗ Turn {turn_idx} processing exception: {e}")
                    
                    await asyncio.sleep(0.05)
                
                if simulation_interrupted:
                    break
                
                # ✅ Inter-session L2 backfill: This is only triggered when there are multiple sessions on the same day.
                # Trigger conditions:
                # 1. There are multiple sessions on the same day (len(day_sessions) > 1).
                # 2. The current session is not the last one (session_idx < len(day_sessions)).
                # Purpose: To generate L2 memories for a session immediately after it ends, avoiding waiting until the next day.
                if len(day_sessions) > 1 and session_idx < len(day_sessions):
                    print(f"\n  🔄 Inter-session L2 backfill detection ({len(day_sessions)} sessions today, current session {session_idx})")
                    
                    try:
                        # 🔧 Change: The L2 backfill time has been changed to 23:59:59 on the session day.
                        from datetime import time as dt_time
                        session_date = session_time.date()
                        l2_timestamp = datetime.combine(session_date, dt_time(23, 59, 59))
                        print(f"      Session {session_id} start time: {session_time.strftime('%Y-%m-%d %H:%M:%S')}")
                        print(f"      Session {session_id} last turn time: {session_last_turn_timestamp.strftime('%Y-%m-%d %H:%M:%S')}")
                        print(f"      Session {session_id} L2 generation time (end of day): {l2_timestamp.strftime('%Y-%m-%d %H:%M:%S')}")
                        
                        detector = CatchUpDetector()
                        l2_tasks = await detector.detect_specific_session_l2(
                            user_id=user_id,
                            expert_id=expert_id,
                            session_id=f"{conv_id}_{session_id}",
                            timestamp=l2_timestamp
                        )
                        
                        if l2_tasks:
                            sorter = BackfillTaskSorter()
                            sorted_tasks = sorter.sort_tasks(l2_tasks)
                            result = await service.run_backfill(sorted_tasks)
                            
                            # Count the generated L2 memories.
                            l2_count = 0
                            generated_memories = result.memories if hasattr(result, 'memories') else result.get("generated_memories", [])
                            for memory in generated_memories:
                                mem_level = None
                                if hasattr(memory, 'level'):
                                    mem_level = memory.level
                                elif isinstance(memory, dict) and 'level' in memory:
                                    mem_level = memory['level']
                                if mem_level:
                                    mem_level_str = mem_level.value if hasattr(mem_level, 'value') else str(mem_level)
                                    if "L2" in mem_level_str:
                                        l2_count += 1
                            
                            total_l2 += l2_count
                            inter_session_l2 += l2_count
                            day_log["l2_generated"] += l2_count
                            
                            # 🆕 Record statistics.
                            if stats_helper:
                                stats_helper.record_memories_from_result(
                                    result,
                                    trigger_type="Inter-session backfill"
                                )
                            
                            print(f"      ✅ Inter-session L2 backfill completed: {l2_count} memories")
                        else:
                            print(f"      ⏭ L2 memories already exist. No backfill needed.")
                            
                    except Exception as e:
                        logger.error(f"Inter-session L2 backfill exception: {e}")
                        print(f"      ✗ Inter-session L2 backfill exception: {e}")
            
            if simulation_interrupted:
                break
        else:
            print(f"⏭ No sessions today.")
        
        # If the process is interrupted, break out of the day loop.
        if simulation_interrupted:
            break
        
        # Record the daily log.
        stats.log_day(current_date, day_log)
        stats.total_days += 1
        if day_log["has_sessions"]:
            stats.days_with_sessions += 1
        if day_log["backfill_triggered"]:
            stats.days_with_backfill += 1
        if not day_log["has_sessions"] and not day_log["backfill_triggered"]:
            stats.days_skipped += 1
        
        # Move to the next day.
        current_date += timedelta(days=1)
        await asyncio.sleep(0.1)
    
    # If the process is interrupted, return early.
    if simulation_interrupted:
        print(f"\n[REALISTIC_SIM] ⚠️ Simulation interrupted. Returning partial results...")
        return {
            "conv_id": conv_id,
            "success": False,
            "error": "Simulation interrupted by user",
            "total_turns": total_turns,
            "total_l1": total_l1,
            "total_l2": total_l2
        }
    
    # ✅ Final backfill: Run on the day after the last session day.
    print(f"\n{'='*80}")
    print(f"[REALISTIC_SIM] Final backfill: last session day+1")
    print(f"{'='*80}")
    
    final_timestamp = simulation_end_date + timedelta(days=1)
    print(f"  Backfill time: {final_timestamp.strftime('%Y-%m-%d')} 02:00")
    
    try:
        detector = CatchUpDetector()
        
        # Step 1: Perform a regular backfill to detect missing memories from the last session day.
        print(f"\n  🔍 Step 1: Regular backfill (detecting missing memories from yesterday...)")
        regular_tasks = await detector.detect_missing_in_recent_months(
            user_id=user_id,
            expert_id=expert_id,
            month_count=2,
            force=False,
            force_timestamp=datetime.combine(final_timestamp, datetime.min.time()) + timedelta(hours=2)
        )
        
        if regular_tasks:
            print(f"      📋 Pending backfill tasks: {len(regular_tasks)}")
            for task in regular_tasks:
                print(f"          - {task.layer}: {task.session_id or task.time_window}")
            
            sorter = BackfillTaskSorter()
            sorted_regular_tasks = sorter.sort_tasks(regular_tasks)
            result = await service.run_backfill(sorted_regular_tasks)
            
            # Count the generated memories.
            generated_memories = result.memories if hasattr(result, 'memories') else result.get("generated_memories", [])
            regular_l2_count = regular_l3_count = regular_l4_count = regular_l5_count = 0
            for memory in generated_memories:
                # Try multiple ways to get the memory level.
                mem_level = None
                if hasattr(memory, 'level'):
                    mem_level = memory.level
                elif isinstance(memory, dict) and 'level' in memory:
                    mem_level = memory['level']
                    
                if mem_level:
                    mem_level_str = mem_level.value if hasattr(mem_level, 'value') else str(mem_level)
                    if "L2" in mem_level_str:
                        total_l2 += 1
                        regular_l2_count += 1
                    elif "L3" in mem_level_str:
                        total_l3 += 1
                        regular_l3_count += 1
                    elif "L4" in mem_level_str:
                        total_l4 += 1
                        regular_l4_count += 1
                    elif "L5" in mem_level_str:
                        total_l5 += 1
                        regular_l5_count += 1
            
            # 🆕 Record statistics.
            if stats_helper:
                stats_helper.record_memories_from_result(
                    result,
                    trigger_type="Daily auto-backfill"
                )
            
            print(f"      ✅ Auto-backfill completed: L2={regular_l2_count}, L3={regular_l3_count}, L4={regular_l4_count}, L5={regular_l5_count}")
        else:
            print(f"      ⏭ No pending backfill tasks.")
        
        # Step 2: Perform a force backfill to fill in missing weeks/months for L4/L5 memories.
        print(f"\n  ⚡ Step 2: Force backfill (for missing L4/L5 memories in recent weeks/months).")
        force_tasks = await detector.detect_missing_in_recent_months(
            user_id=user_id,
            expert_id=expert_id,
            month_count=2,
            force=True,
            force_timestamp=datetime.combine(final_timestamp, datetime.min.time()) + timedelta(hours=2)
        )
        
        if force_tasks:
            print(f"      📋 Pending backfill tasks: {len(force_tasks)}")
            for task in force_tasks:
                print(f"          - {task.layer}: {task.time_window}, force_update={task.force_update}")
            
            sorter = BackfillTaskSorter()
            sorted_force_tasks = sorter.sort_tasks(force_tasks)
            result = await service.run_backfill(sorted_force_tasks)
            
            # Count the generated memories.
            generated_memories = result.memories if hasattr(result, 'memories') else result.get("generated_memories", [])
            force_l4_count = force_l5_count = 0
            for memory in generated_memories:
                # Try multiple ways to get the memory level.
                mem_level = None
                if hasattr(memory, 'level'):
                    mem_level = memory.level
                elif isinstance(memory, dict) and 'level' in memory:
                    mem_level = memory['level']
                    
                if mem_level:
                    mem_level_str = mem_level.value if hasattr(mem_level, 'value') else str(mem_level)
                    if "L4" in mem_level_str:
                        total_l4 += 1
                        force_l4_count += 1
                    elif "L5" in mem_level_str:
                        total_l5 += 1
                        force_l5_count += 1
            
            # 🆕 Record statistics.
            if stats_helper:
                stats_helper.record_memories_from_result(
                    result,
                    trigger_type="Force backfill"
                )
            
            print(f"      ✅ Force backfill completed: L4={force_l4_count}, L5={force_l5_count}")
            force_backfill += 1
        else:
            print(f"      ⏭ No pending backfill tasks.")
        
    except Exception as e:
        print(f"  ❌ Final backfill failed: {e}")
        import traceback
        traceback.print_exc()
    
    # Update the statistics.
    stats.total_sessions = sum(len(sessions) for sessions in sessions_by_date.values())
    stats.total_turns = total_turns
    stats.total_l1_generated = total_l1
    stats.total_l2_generated = total_l2
    stats.total_l3_generated = total_l3
    stats.total_l4_generated = total_l4
    stats.total_l5_generated = total_l5
    stats.inter_session_l2_count = inter_session_l2
    stats.daily_backfill_count = daily_backfill
    stats.force_backfill_count = force_backfill
    
    print(f"\n{'='*80}")
    print(f"[REALISTIC_SIM] Simulation completed: {conv_id}")
    print(f"{'='*80}\n")
    
    return {
        "conv_id": conv_id,
        "success": True,
        "total_days": stats.total_days,
        "total_sessions": stats.total_sessions,
        "total_turns": total_turns,
        "total_l1": total_l1,
        "total_l2": total_l2,
        "total_l3": total_l3,
        "total_l4": total_l4,
        "total_l5": total_l5,
        "inter_session_l2": inter_session_l2,
        "daily_backfill": daily_backfill,
        "force_backfill": force_backfill
    }


async def process_conversation_wrapper(
    conv_id: str,
    conv_data: dict,
    conversation_users: dict,
    service: MemoryGenerationService,
    conv_idx: int,
    total_convs: int,
    semaphore: asyncio.Semaphore,
    config: ParallelSimConfig,
    stats_helper: Optional[StatsTestHelper] = None
) -> Dict[str, Any]:
    """
    Wrapper function for processing a single conversation in parallel.
    """
    async with semaphore:
        start_time = time.perf_counter()
        
        try:
            print(f"\n[PARALLEL_SIM] [{conv_idx}/{total_convs}] Start processing {conv_id}")
            
            # Create independent statistics for each conversation.
            conv_stats = RealisticSimStats()
            conv_stats.start_time = start_time
            
            result = await process_realistic_simulation(
                conv_id=conv_id,
                conv_data=conv_data,
                conversation_users=conversation_users,
                service=service,
                stats=conv_stats,
                conv_idx=conv_idx,
                total_convs=total_convs,
                stats_helper=stats_helper
            )
            
            end_time = time.perf_counter()
            processing_time = end_time - start_time
            
            result.update({
                "processing_time": processing_time,
                "thread_id": threading.get_ident(),
                "stats": conv_stats.get_summary()
            })
            
            print(f"\n[PARALLEL_SIM] [{conv_idx}/{total_convs}] ✅ {conv_id} processing completed, elapsed: {processing_time:.2f}s")
            
            return result
            
        except Exception as e:
            end_time = time.perf_counter()
            processing_time = end_time - start_time
            
            logger.error(f"[{conv_id}] Processing exception: {e}")
            print(f"\n[PARALLEL_SIM] [{conv_idx}/{total_convs}] ❌ {conv_id} processing exception: {e}")
            
            return {
                "conv_id": conv_id,
                "success": False,
                "error": str(e),
                "processing_time": processing_time,
                "thread_id": threading.get_ident()
            }


def load_all_conversations():
    """Load all conversation data from metadata."""
    metadata_file = "data/locomo_experiment_metadata.json"
    try:
        with open(metadata_file, 'r', encoding='utf-8') as f:
            metadata = json.load(f)
        
        conversations = metadata["conversations"]
        conv_ids = list(conversations.keys())
        
        logger.info(f"Loaded {len(conv_ids)} conversations.")
        
    except FileNotFoundError:
        logger.warning(f"Metadata file not found: {metadata_file}")
        return {}, []
    except Exception as e:
        logger.error(f"Failed to load metadata: {e}")
        return {}, []
    
    conversations_data = {}
    
    for conv_id in conv_ids:
        conv_info = conversations[conv_id]
        speakers = conv_info["speakers"]
        sessions_list = conv_info["sessions"]
        
        sessions_data = {}
        for session_info in sessions_list:
            session_id = session_info["session_id"]
            file_path = f"data/locomo10_smart_split/locomo10_timem_{conv_id}_{session_id}.json"
            
            try:
                with open(file_path, 'r', encoding='utf-8') as f:
                    session_data = json.load(f)
                    session_data["speaker_a"] = session_info.get("speaker_a", speakers[0])
                    session_data["speaker_b"] = session_info.get("speaker_b", speakers[1])
                    sessions_data[session_id] = session_data
            except FileNotFoundError:
                logger.warning(f"File not found: {file_path}")
            except Exception as e:
                logger.error(f"Failed to load {conv_id} {session_id}: {e}")
        
        conversations_data[conv_id] = {
            "conv_id": conv_id,
            "speakers": speakers,
            "sessions": sessions_data,
            "total_sessions": len(sessions_data)
        }
        
    logger.info(f"Loading complete: {len(conversations_data)} conversations.")
    return conversations_data, conv_ids


