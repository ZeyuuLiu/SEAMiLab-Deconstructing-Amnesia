"""
Memory generation workflow realistic system simulation test (Locomo dataset)

Usage / Parameters
------------------
This script is designed to be executed via pytest.

It does **NOT** implement custom CLI arguments (no argparse / click / typer).
All configuration is provided via **environment variables** and **config files**.

Recommended commands:

- Run with pytest (default):
    pytest experiments/datasets/locomo/01_memory_generation.py -v

- Run only this test function:
    pytest experiments/datasets/locomo/01_memory_generation.py -k test_realistic_system_simulation -v

- Limit failures:
    pytest experiments/datasets/locomo/01_memory_generation.py -k test_realistic_system_simulation --maxfail=1 -v

Environment variables used:

- TIMEM_DATASET_PROFILE:
    Required. Must be "default" (or "test" if allow_test=True).
    Example (PowerShell):
        $env:TIMEM_DATASET_PROFILE="default"

- OPENAI_API_KEY:
    Optional depending on llm.default_provider / adapter selection.
    Example (PowerShell):
        $env:OPENAI_API_KEY="<your_key>"

Files required:

- data/locomo10_smart_split/...
    Generated by: python dataset_utils/dataset_splitter.py --split-locomo

- data/locomo_experiment_metadata.json
    Generated by the Locomo splitter (same command as above, after our patch).

Important: This test can only run under default dataset configuration
    - Need to set first: export TIMEM_DATASET_PROFILE=default
    - Or in .env file: TIMEM_DATASET_PROFILE=default
    - Allowed to test under test dataset

Simulates the real system operation:
1. Auto-backfill at midnight (from the first session day to the last session day + 1)
2. Handles multiple sessions per day with inter-session L2 backfill to simulate session inactivity
3. Uses force mode for a forced backfill on the last day
4. Fully observes when the system backfills and when it skips

Core improvements:
- Real-time flow simulation
- Simulation of the system's auto-trigger mechanism
- Simulation of inter-session L2 generation
- Detailed logging for observing system behavior
- Added a dataset configuration guard to prevent misoperation
"""

