    parser.add_argument('--dataset', type=str,
                       help='Dataset name (e.g., locomo, longmemeval_s), for loading corresponding config file')
    parser.add_argument('--out-file', type=str,
                       help='Result output file path (auto-generated if not specified)')
    parser.add_argument('--model-name', type=str, default='timem-memory-retrieval',
                       help='Model name')
    parser.add_argument('--enable-exact-match', action='store_true',
                       help='Enable Exact Match evaluation (disabled by default)')
    parser.add_argument('--enable-recall', action='store_true',
                       help='Enable Recall evaluation (disabled by default)')
    parser.add_argument('--verbose', action='store_true',
                       help='Verbose output')
    parser.add_argument('--bert-model-dir', type=str, default=BERT_MODEL_DIR,
                       help='BERT model directory path')
    parser.add_argument('--batch-size', type=int, default=BATCH_SIZE,
                       help='Batch evaluation size (default 32)')
    parser.add_argument('--no-cuda', action='store_true',
                       help='Disable CUDA, force CPU usage')
    parser.add_argument('--auto-latest', action='store_true',
                       help='Auto-find latest evaluation data file (mutually exclusive with --data-file)')
    parser.add_argument('--timestamp-only', action='store_true',
                       help='Only find files with standard timestamp format (YYYYMMDD_HHMMSS)')
    parser.add_argument('--disable-llm-eval', action='store_true',
                       help='Disable LLM evaluation (enabled by default)')
    parser.add_argument('--llm-only', action='store_true',
                       help='Enable LLM evaluation only, skip all traditional metrics (F1, RL, BLEU, etc.)')
    parser.add_argument('--use-glm', action='store_true',
                       help='Use GLM-4-flash for LLM evaluation (default use OpenAI)')
    parser.add_argument('--limit', type=int, default=0,
                       help='Limit evaluation sample count (0 means all, >0 means limit)')
    parser.add_argument('--num-runs', type=int, default=1,
                       help='Number of LLM evaluation repetitions (default 1)')
    args = parser.parse_args()
    
    # Default enable auto-find latest file
    if not args.auto_latest and not args.data_file:
        args.auto_latest = True
        args.verbose = True  # Enable verbose output when running directly
        print("💡 Direct run mode: auto-enable --auto-latest and --verbose")
    
    # Validate parameters
    if args.auto_latest and args.data_file:
        parser.error("--auto-latest and --data-file cannot be used together")
    
    # Validate LLM evaluation parameter conflicts
    if args.disable_llm_eval and args.llm_only:
        parser.error("--disable-llm-eval and --llm-only cannot be used together")
    
    # If --llm-only enabled, force enable LLM evaluation
    if args.llm_only:
        args.disable_llm_eval = False
        print(f"💡 Enable --llm-only mode: calculate LLM evaluation only, skip all traditional metrics")
    
    # If output file not specified, generate default output file path
    if not args.out_file:
        args.out_file = "logs/tests/latest_evaluation_results.json"
        print(f"💡 Output file not specified, using default path: {args.out_file}")
    
    return args


