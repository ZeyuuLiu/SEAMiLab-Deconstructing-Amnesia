def load_eval_prompt(dataset_name: str = None):
    """
    Load evaluation prompts and LLM configuration
    
    Args:
        dataset_name: Dataset name (e.g., 'locomo', 'longmemeval_s'), if None use global config
        
    Returns:
        (Evaluation prompt content, LLM configuration dictionary)
    """
    # Determine configuration file path
    if dataset_name:
        # Prioritize dataset-specific configuration
        dataset_eval_prompt_file = f"config/datasets/{dataset_name}/eval_prompt.yaml"
        
        # Check if dataset-specific configuration exists
        if Path(dataset_eval_prompt_file).exists():
            eval_prompt_file = dataset_eval_prompt_file
            print(f"📋 Using dataset-specific evaluation prompt: {eval_prompt_file}")
        else:
            # Fallback to global configuration
            eval_prompt_file = DEFAULT_EVAL_PROMPT_FILE
            print(f"⚠️ Dataset-specific configuration not found, using global configuration: {eval_prompt_file}")
    else:
        eval_prompt_file = DEFAULT_EVAL_PROMPT_FILE
        print(f"📋 Using global evaluation prompt: {eval_prompt_file}")
    
    try:
        with open(eval_prompt_file, 'r', encoding='utf-8') as f:
            content = f.read()
        
        # Try to parse YAML to extract actual prompt and LLM configuration
        try:
            import yaml
            prompt_data = yaml.safe_load(content)
            
            if isinstance(prompt_data, dict):
                # Extract prompt
                prompt_template = prompt_data.get('qa_evaluation_prompt', content)
                
                # 🔧 Extract LLM configuration
                llm_config = prompt_data.get('eval_llm_config', {})
                
                if llm_config:
                    print(f"✅ Evaluation LLM configuration loaded successfully:")
                    print(f"   Provider: {llm_config.get('provider', 'N/A')}")
                    print(f"   Model: {llm_config.get('model', 'N/A')}")
                else:
                    print(f"⚠️ eval_llm_config not found, will use default configuration")
                
                print(f"✅ Evaluation prompt loaded successfully (YAML format): {eval_prompt_file}")
                return prompt_template, llm_config
            else:
                # If not a dictionary, return raw content and empty config
                print(f"✅ Evaluation prompt loaded successfully (raw format): {eval_prompt_file}")
                return content, {}
        except Exception as e:
            # YAML parsing failed, return raw content and empty config
            print(f"⚠️ YAML parsing failed: {e}")
            print(f"✅ Evaluation prompt loaded successfully (raw format): {eval_prompt_file}")
            return content, {}
            
    except Exception as e:
        print(f"❌ Evaluation prompt loading failed: {e}")
        return None, {}

