def safe_str(obj, max_depth=5, current_depth=0):
    """
    Safe string conversion function, avoid recursion depth issues from circular references
    
    Args:
        obj: Object to convert
        max_depth: Maximum recursion depth
        current_depth: Current recursion depth
        
    Returns:
        Safe string representation
    """
    if current_depth >= max_depth:
        return "<max_depth_reached>"
    
    try:
        # Handle None value
        if obj is None:
            return ""
        
        # Handle basic data types
        if isinstance(obj, (str, int, float, bool)):
            return str(obj)
        
        # Handle lists and tuples
        if isinstance(obj, (list, tuple)):
            if len(obj) == 0:
                return ""
            # Only take first element, avoid deep recursion
            return safe_str(obj[0], max_depth, current_depth + 1)
        
        # Handle dictionaries
        if isinstance(obj, dict):
            if not obj:
                return ""
            # Try to get common text fields
            for key in ['text', 'content', 'message', 'value', 'data']:
                if key in obj:
                    return safe_str(obj[key], max_depth, current_depth + 1)
            # If no common field found, return first value
            first_key = next(iter(obj))
            return safe_str(obj[first_key], max_depth, current_depth + 1)
        
        # For other objects, try simple string conversion
        return str(obj)[:1000]  # Limit length to prevent overly long strings
        
    except (RecursionError, ValueError, TypeError) as e:
        return f"<conversion_error: {type(e).__name__}>"
    except Exception:
        return "<unknown_object>"


def safe_normalize_text(text):
    """
    Safe text normalization function
    
    Args:
        text: Input text
        
    Returns:
        Normalized text string
    """
    try:
        # First perform safe string conversion
        text_str = safe_str(text)
        
        # If result is empty or error marker, return empty string
        if not text_str or text_str.startswith('<'):
            return ""
        
        # Limit text length to avoid performance issues from overly long text
        if len(text_str) > 10000:
            text_str = text_str[:10000] + "..."
        
        return text_str
        
    except Exception:
        return ""

