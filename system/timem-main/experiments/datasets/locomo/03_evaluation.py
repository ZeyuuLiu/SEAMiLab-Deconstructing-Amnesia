    def _calculate_similarity(self, prediction: str, reference: str) -> float:
        """Calculate semantic similarity (using sentence-transformers)"""
        if not SENTENCE_TRANSFORMER_AVAILABLE or self.sim_model is None:
            return 0.0
        
        try:
            # Generate sentence embeddings
            embeddings = self.sim_model.encode([prediction, reference], convert_to_tensor=True)
            
            # Calculate cosine similarity
            pred_emb = embeddings[0].cpu().numpy()
            ref_emb = embeddings[1].cpu().numpy()
            
            # Cosine similarity: 1 - cosine distance
            similarity = 1.0 - cosine(pred_emb, ref_emb)
            
            return max(0.0, min(1.0, similarity))
            
        except Exception as e:
            print(f"⚠️ Sim calculation exception: {e}")
            return 0.0
    
