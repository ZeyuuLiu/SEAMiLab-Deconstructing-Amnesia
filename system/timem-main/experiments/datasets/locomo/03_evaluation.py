    def evaluate_qa_pair(self, question: str, answer: str, prediction: str) -> Dict[str, float]:
        """Evaluate single QA pair - calculate all hard metrics"""
        results = {}
        
        # Safely normalize input text
        try:
            safe_question = safe_normalize_text(question)
            safe_answer = safe_normalize_text(answer)
            safe_prediction = safe_normalize_text(prediction)
        except Exception as e:
            print(f"⚠️ Text normalization failed: {e}")
            return self._get_empty_scores()
        
        if not safe_prediction or safe_prediction.strip() == "":
            # If prediction is empty, all scores are 0
            return self._get_empty_scores()
        
        try:
            # F1 Score - use original framework implementation
            if self.supported_metrics['f1_score']:
                try:
                    results['f1_score'] = f1_score(safe_prediction, safe_answer)
                except Exception as e:
                    print(f"⚠️ F1 Score calculation failed: {e}")
                    results['f1_score'] = 0.0
            
            # BERT Score - removed, use BERTScore-F1 instead
            if self.supported_metrics['bert_score']:
                # Use BERTScore-F1 instead of original BERT Score
                try:
                    results['bert_score'] = self._calculate_bertscore_f1(safe_prediction, safe_answer)
                except Exception as e:
                    print(f"⚠️ BERT Score calculation failed: {e}")
                    results['bert_score'] = 0.0
            
            # Rouge-L Score - use original framework implementation
            if self.supported_metrics['rouge_l_score']:
                try:
                    results['rouge_l_score'] = rougel_score(safe_prediction, safe_answer)
                except Exception as e:
                    print(f"⚠️ Rouge-L Score calculation failed: {e}")
                    results['rouge_l_score'] = 0.0
            
            # BLEU-1 Score
            if self.supported_metrics['bleu_1']:
                try:
                    results['bleu_1'] = self._calculate_bleu(safe_prediction, safe_answer, n=1)
                except Exception as e:
                    print(f"⚠️ BLEU-1 calculation failed: {e}")
                    results['bleu_1'] = 0.0
            
            # BLEU-2 Score
            if self.supported_metrics['bleu_2']:
                try:
                    results['bleu_2'] = self._calculate_bleu(safe_prediction, safe_answer, n=2)
                except Exception as e:
                    print(f"⚠️ BLEU-2 calculation failed: {e}")
                    results['bleu_2'] = 0.0
            
            # METEOR Score
            if self.supported_metrics['meteor']:
                try:
                    results['meteor'] = self._calculate_meteor(safe_prediction, safe_answer)
                except Exception as e:
                    print(f"⚠️ METEOR calculation failed: {e}")
                    results['meteor'] = 0.0
            
            # BERTScore-F1
            if self.supported_metrics['bertscore_f1']:
                try:
                    results['bertscore_f1'] = self._calculate_bertscore_f1(safe_prediction, safe_answer)
                except Exception as e:
                    print(f"⚠️ BERTScore-F1 calculation failed: {e}")
                    results['bertscore_f1'] = 0.0
            
            # Semantic Similarity (Sim)
            if self.supported_metrics['sim']:
                try:
                    results['sim'] = self._calculate_similarity(safe_prediction, safe_answer)
                except Exception as e:
                    print(f"⚠️ Sim calculation failed: {e}")
                    results['sim'] = 0.0
            
            # Exact Match (disabled)
            if self.supported_metrics['exact_match']:
                # Temporarily disabled, interface preserved
                results['exact_match'] = 0.0
            
            # Recall (disabled)
            if self.supported_metrics['recall']:
                # Temporarily disabled, interface preserved
                results['recall'] = 0.0
                
        except Exception as e:
            print(f"⚠️ Evaluation error - Question: {safe_normalize_text(question)[:50]}..., Error: {e}")
            results = self._get_empty_scores()
        
        return results
    
    def _get_empty_scores(self) -> Dict[str, float]:
        """Return empty scores dictionary"""
        scores = {}
        for metric in self.supported_metrics:
            if self.supported_metrics[metric]:
                scores[metric] = 0.0
        return scores
    
    def _batch_normalize_and_stem(self, texts: List[str]) -> List[List[str]]:
        """Batch normalize and stem extraction (highly optimized)"""
        if not texts:
            return []
        
        try:
            # Vectorized normalize (list comprehension faster than loop)
            normalized_texts = [normalize_answer(text) if text else "" for text in texts]
            
            # Vectorized tokenization and stem
            if ps:
                result = [[ps.stem(w) for w in text.split()] if text else [] for text in normalized_texts]
            else:
                result = [text.split() if text else [] for text in normalized_texts]
            
            return result
        except Exception as e:
            print(f"⚠️ Batch normalize and stem failed: {e}")
            return [[]] * len(texts)
    
    def _compute_all_metrics_vectorized(self, predictions: List[str], references: List[str]) -> List[Dict[str, float]]:
        """Vectorized calculation of all traditional metrics (engineering-level high-performance implementation)
        
        One-time preprocessing, parallel calculation of all metrics, avoid duplicate operations
        """
        n = len(predictions)
        if n == 0 or len(references) != n:
            return [self._get_empty_scores() for _ in range(n)]
        
        print(f"🚀 Vectorized calculation of all traditional metrics for {n} QA pairs...")
        
        # ============ Phase 1: Data preprocessing (one-time completion) ============
        # Preprocess normalize + stem (needed for F1 and RL)
        pred_tokens_stemmed = self._batch_normalize_and_stem(predictions)
        ref_tokens_stemmed = self._batch_normalize_and_stem(references)
        
        # Preprocess lowercase tokenization (needed for BLEU and METEOR)
        pred_tokens_lower = [p.lower().split() if p else [] for p in predictions]
        ref_tokens_lower = [r.lower().split() if r else [] for r in references]
        
        # ============ Phase 2: Parallel calculation of all metrics ============
        results = [self._get_empty_scores() for _ in range(n)]
        
        # F1 Score (using preprocessed stemmed tokens)
        if self.supported_metrics['f1_score']:
            print("  ⚡ F1...")
            for i, (pred_tok, ref_tok) in enumerate(zip(pred_tokens_stemmed, ref_tokens_stemmed)):
                if pred_tok and ref_tok:
                    common = Counter(pred_tok) & Counter(ref_tok)
                    num_same = sum(common.values())
                    if num_same > 0:
                        p = num_same / len(pred_tok)
                        r = num_same / len(ref_tok)
                        results[i]['f1_score'] = min(1.0, (2 * p * r) / (p + r))
        
        # Rouge-L (batch call Rouge API)
        if self.supported_metrics['rouge_l_score']:
            print("  ⚡ Rouge-L...")
            try:
                from rouge import Rouge
                rouge = Rouge()
                valid_pairs = [(i, ' '.join(pred_tokens_stemmed[i]), ' '.join(ref_tokens_stemmed[i])) 
                              for i in range(n) if pred_tokens_stemmed[i] and ref_tokens_stemmed[i]]
                if valid_pairs:
                    indices, pred_strs, ref_strs = zip(*valid_pairs)
                    try:
                        rouge_scores = rouge.get_scores(list(pred_strs), list(ref_strs))
                        for idx, score_dict in zip(indices, rouge_scores):
                            results[idx]['rouge_l_score'] = min(1.0, score_dict['rouge-l']['f'])
                    except:
                        pass
            except Exception as e:
                print(f"    ⚠️ Rouge-L failed: {e}")
        
        # BLEU-1 and BLEU-2 (using preprocessed lower tokens)
        if self.supported_metrics['bleu_1'] or self.supported_metrics['bleu_2']:
            print("  ⚡ BLEU...")
            if NLTK_AVAILABLE:
                smoothing = SmoothingFunction().method1
                if self.supported_metrics['bleu_1']:
                    for i, (pred_tok, ref_tok) in enumerate(zip(pred_tokens_lower, ref_tokens_lower)):
                        if pred_tok and ref_tok:
                            try:
                                score = sentence_bleu([ref_tok], pred_tok, weights=(1.0,0,0,0), smoothing_function=smoothing)
                                results[i]['bleu_1'] = min(1.0, score)
                            except:
                                pass
                if self.supported_metrics['bleu_2']:
                    for i, (pred_tok, ref_tok) in enumerate(zip(pred_tokens_lower, ref_tokens_lower)):
                        if pred_tok and ref_tok:
                            try:
                                score = sentence_bleu([ref_tok], pred_tok, weights=(0.5,0.5,0,0), smoothing_function=smoothing)
                                results[i]['bleu_2'] = min(1.0, score)
                            except:
                                pass
        
        # METEOR (using preprocessed lower tokens)
        if self.supported_metrics['meteor']:
            print("  ⚡ METEOR...")
            if NLTK_AVAILABLE:
                for i, (pred_tok, ref_tok) in enumerate(zip(pred_tokens_lower, ref_tokens_lower)):
                    if pred_tok and ref_tok:
                        try:
                            results[i]['meteor'] = min(1.0, nltk_meteor_score([ref_tok], pred_tok))
                        except:
                            pass
        
        # BERTScore (GPU batch calculation)
        if self.supported_metrics['bertscore_f1'] and self.bert_model_loaded:
            print("  ⚡ BERTScore (GPU)...")
            try:
                valid_indices = [i for i in range(n) if predictions[i] and references[i]]
                if valid_indices:
                    valid_preds = [predictions[i] for i in valid_indices]
                    valid_refs = [references[i] for i in valid_indices]
                    scores = self._calculate_bertscore_f1_batch(valid_preds, valid_refs)
                    for idx, score in zip(valid_indices, scores):
                        results[idx]['bertscore_f1'] = score
            except Exception as e:
                print(f"    ⚠️ BERTScore failed: {e}")
        
        # Similarity (batch encoding)
        if self.supported_metrics['sim'] and SENTENCE_TRANSFORMER_AVAILABLE and self.sim_model:
            print("  ⚡ Similarity...")
            try:
                all_texts = [text if text else "" for pair in zip(predictions, references) for text in pair]
                embeddings = self.sim_model.encode(all_texts, convert_to_tensor=True, show_progress_bar=False, batch_size=128)
                embeddings_np = embeddings.cpu().numpy()
                for i in range(n):
                    try:
                        sim = 1.0 - cosine(embeddings_np[2*i], embeddings_np[2*i+1])
                        results[i]['sim'] = min(1.0, max(0.0, sim))
                    except:
                        pass
            except Exception as e:
                print(f"    ⚠️ Similarity failed: {e}")
        
        print(f"✅ Vectorized calculation completed")
        return results
    
    def _batch_calculate_f1(self, predictions: List[str], references: List[str]) -> List[float]:
        """Batch calculate F1 score (efficient optimized version)"""
        n = len(predictions)
        if not predictions or not references or n != len(references):
            return [0.0] * n
        
        try:
            # Batch normalize and stem (one-time processing)
            pred_tokens_list = self._batch_normalize_and_stem(predictions)
            ref_tokens_list = self._batch_normalize_and_stem(references)
            
            # Vectorized F1 calculation
            scores = []
            for pred_tokens, ref_tokens in zip(pred_tokens_list, ref_tokens_list):
                if not pred_tokens or not ref_tokens:
                    scores.append(0.0)
                else:
                    common = Counter(pred_tokens) & Counter(ref_tokens)
                    num_same = sum(common.values())
                    if num_same == 0:
                        scores.append(0.0)
                    else:
                        precision = num_same / len(pred_tokens)
                        recall = num_same / len(ref_tokens)
                        scores.append(min(1.0, (2 * precision * recall) / (precision + recall)))
            
            return scores
        except Exception as e:
            print(f"⚠️ Batch F1 calculation failed: {e}")
            return [0.0] * n
    
    def _batch_calculate_rougel(self, predictions: List[str], references: List[str]) -> List[float]:
        """Batch calculate Rouge-L score (efficient optimized version)"""
        n = len(predictions)
        if not predictions or not references or n != len(references):
            return [0.0] * n
        
        try:
            from rouge import Rouge
            rouge = Rouge()
            
            # Batch normalize and stem (one-time processing)
            pred_tokens_list = self._batch_normalize_and_stem(predictions)
            ref_tokens_list = self._batch_normalize_and_stem(references)
            
            # Batch convert to string and calculate
            valid_pairs = []
            valid_indices = []
            for i, (pred_tokens, ref_tokens) in enumerate(zip(pred_tokens_list, ref_tokens_list)):
                if pred_tokens and ref_tokens:
                    valid_pairs.append((' '.join(pred_tokens), ' '.join(ref_tokens)))
                    valid_indices.append(i)
            
            # Initialize results
            scores = [0.0] * n
            
            # Batch calculate valid pairs
            if valid_pairs:
                try:
                    pred_strs = [p[0] for p in valid_pairs]
                    ref_strs = [p[1] for p in valid_pairs]
                    rouge_scores = rouge.get_scores(pred_strs, ref_strs)
                    for idx, score_dict in zip(valid_indices, rouge_scores):
                        scores[idx] = min(1.0, score_dict['rouge-l']['f'])
                except Exception:
                    # Downgrade to individual calculation
                    for idx, (pred_str, ref_str) in zip(valid_indices, valid_pairs):
                        try:
                            score_dict = rouge.get_scores(pred_str, ref_str, avg=True)
                            scores[idx] = min(1.0, score_dict['rouge-l']['f'])
                        except Exception:
                            scores[idx] = 0.0
            
            return scores
        except Exception as e:
            print(f"⚠️ Batch Rouge-L calculation failed: {e}")
            return [0.0] * n
    
    def _batch_calculate_bleu(self, predictions: List[str], references: List[str], n: int = 1) -> List[float]:
        """Batch calculate BLEU score (efficient optimized version)"""
        m = len(predictions)
        if not NLTK_AVAILABLE or not predictions or not references or m != len(references):
            return [0.0] * m
        
        try:
            # Pre-set weights and smoothing function (avoid repeated creation)
            weights = (1.0, 0, 0, 0) if n == 1 else (0.5, 0.5, 0, 0) if n == 2 else (1.0 / n,) * n + (0,) * (4 - n)
            smoothing = SmoothingFunction().method1
            
            # Batch tokenization (one-time processing)
            pred_tokens_list = [pred.lower().split() if pred else [] for pred in predictions]
            ref_tokens_list = [ref.lower().split() if ref else [] for ref in references]
            
            # Batch calculate BLEU
            scores = []
            for pred_tokens, ref_tokens in zip(pred_tokens_list, ref_tokens_list):
                if pred_tokens and ref_tokens:
                    try:
                        score = sentence_bleu([ref_tokens], pred_tokens, weights=weights, smoothing_function=smoothing)
                        scores.append(min(1.0, score))
                    except Exception:
                        scores.append(0.0)
                else:
                    scores.append(0.0)
            
            return scores
        except Exception as e:
            print(f"⚠️ Batch BLEU-{n} calculation failed: {e}")
            return [0.0] * m
    
