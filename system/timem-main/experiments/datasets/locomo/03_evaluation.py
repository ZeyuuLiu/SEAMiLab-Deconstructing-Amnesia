def generate_unique_output_path(base_output_file: str) -> str:
    """
    Generate unique output file path to avoid overwriting
    
    Args:
        base_output_file: Base output file path
        
    Returns:
        Unique file path with timestamp
    """
    # Get parts of the file path
    base_path = Path(base_output_file)
    directory = base_path.parent
    stem = base_path.stem
    suffix = base_path.suffix
    
    # Generate timestamp
    timestamp = datetime.now().strftime('%Y%m%d_%H%M%S')
    
    # Build new filename
    unique_filename = f"{stem}_{timestamp}{suffix}"
    unique_path = directory / unique_filename
    
    return str(unique_path)


